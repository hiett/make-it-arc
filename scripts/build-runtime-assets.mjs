// Inlines arc-monkey-patches.js and arc-std-lib/**/*.js into a generated TS
// module so makeItArc can be bundled into a browser build without runtime fs.
import fs from 'node:fs';
import path from 'node:path';
import url from 'node:url';

const ROOT = path.resolve(path.dirname(url.fileURLToPath(import.meta.url)), '..');

const monkey = fs
    .readFileSync(path.join(ROOT, 'arc-monkey-patches.js'), 'utf8')
    .split('// FOOTER_BELOW');
const banner = monkey[0];
const footer = monkey[1] || '';

function collect(dir) {
    const out = [];
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
        const full = path.join(dir, entry.name);
        if (entry.isDirectory()) out.push(...collect(full));
        else if (entry.isFile() && entry.name.endsWith('.js')) out.push(full);
    }
    return out;
}

const stdLibFiles = collect(path.join(ROOT, 'arc-std-lib')).sort();
const stdLib = stdLibFiles
    .map((f) => '// --- arc-std-lib: ' + path.relative(ROOT, f) + ' ---\n' + fs.readFileSync(f, 'utf8'))
    .join('\n');

const out = `// Auto-generated by scripts/build-runtime-assets.mjs. Do not edit.
export const BANNER: string = ${JSON.stringify(banner)};
export const FOOTER: string = ${JSON.stringify(footer)};
export const STD_LIB: string = ${JSON.stringify(stdLib)};
`;

fs.writeFileSync(path.join(ROOT, 'runtime-assets.generated.ts'), out);
console.log('wrote runtime-assets.generated.ts (' + out.length + ' chars)');
